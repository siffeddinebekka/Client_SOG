<h3>Présentation de la socitée SOG</h3>



<br/>
</br>




<p>La société SOG, constructeur informatique, commercialise ses propres systèmes informatiques. Dotée d’un service après-vente important, elle assure la maintenance des systèmes de ses clients lorsque ceux-ci souscrivent un contrat de maintenance. Le service après-vente emploie 150 techniciens répartis sur le territoire national. Les interventions sont assurées par des techniciens pilotés par un centre d'appels national
</p>






